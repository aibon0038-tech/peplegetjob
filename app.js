function weakAppealCopy(item) {
  return `${cleanAppealLabel(item.appeals[0][0])}あり、未経験歓迎、働きやすい職場です。`;
}

function strongAppealCopy(item, appeal, index) {
  const proof = proofPoints(item)[index] || proofPoints(item)[0];
  return `${cleanAppealLabel(appeal[0])}について、${proof}を明記します。例：「${appeal[2]}」のように、制度名だけでなく実際の運用まで書きます。`;
}

function cleanAppealLabel(label) {
  return label.replace("を明記", "").replace("を消す", "").replace("の不安", "").trim();
}

function salesToCopy(check, item) {
  if (check.includes("給与") || check.includes("月収") || check.includes("日給") || check.includes("歩合") || check.includes("手当") || check.includes("年収")) {
    return "最低保証・平均月収・最高月収例・手当条件を分けて記載する。";
  }
  if (check.includes("休日") || check.includes("シフト") || check.includes("残業") || check.includes("夜勤") || check.includes("勤務")) {
    return "平均残業、希望休、有給取得実績、繁忙期の違いを数字で記載する。";
  }
  if (check.includes("教育") || check.includes("研修") || check.includes("資格")) {
    return "研修期間、担当者、費用補助、独り立ちまでの流れを記載する。";
  }
  if (check.includes("人員") || check.includes("職員") || check.includes("スタッフ") || check.includes("定着")) {
    return "人数、年齢層、定着率、面談制度、見学可否を記載する。";
  }
  return `${check}の平均・範囲・例外条件を確認し、応募前に分かる表現へ変換する。`;
}

function painBackground(pain, item) {
  if (payRelated(pain)) return `${item.name}では業務負荷や資格に対して処遇が見合うかを強く見られる。`;
  if (pain.includes("休") || pain.includes("残業") || pain.includes("拘束") || pain.includes("夜勤")) return "生活リズムや家庭との両立に直結し、長期就業の不安になりやすい。";
  if (pain.includes("人間関係") || pain.includes("上司") || pain.includes("園長")) return "少人数・チーム業務では職場の雰囲気が定着率に直結する。";
  if (pain.includes("未経験") || pain.includes("教育") || pain.includes("資格")) return "入社後に何をどこまで教えてもらえるかが見えないと応募をためらう。";
  return `${item.name}特有の働き方や現場実態が見えにくく、応募前の心理的な壁になっている。`;
}

function painSolution(pain, item) {
  if (payRelated(pain)) return item.priorities[0][3];
  if (pain.includes("休") || pain.includes("残業") || pain.includes("拘束") || pain.includes("夜勤")) return item.priorities[1][3];
  if (pain.includes("人間関係") || pain.includes("上司") || pain.includes("園長")) return item.priorities.find((priority) => priority[0].includes("人間") || priority[0].includes("社風"))?.[3] || "職員構成、面談制度、見学可否";
  if (pain.includes("未経験") || pain.includes("教育") || pain.includes("資格")) return item.priorities.find((priority) => priority[0].includes("教育") || priority[0].includes("資格"))?.[3] || "研修内容、資格支援、独り立ち目安";
  return `${item.salesChecks.slice(0, 3).join("、")}を具体的に記載する。`;
}

function payRelated(pain) {
  return ["給与", "日給", "収入", "年収", "手当", "歩合", "処遇", "低い", "上がらない"].some((word) => pain.includes(word));
}

